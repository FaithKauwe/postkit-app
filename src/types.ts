export type PostStatus = 'draft' | 'review' | 'published'

export type Post = {
  id: string
  title: string
  body: string
  author: string
  tags: string[]
  category: string
  status: PostStatus
  createdAt: string
  updatedAt: string
  /** ISO timestamp when status first became `published`; omit until then. ? makes it optional */
  publishedAt?: string
}